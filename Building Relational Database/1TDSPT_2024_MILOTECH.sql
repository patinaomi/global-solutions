-- Relatório usando between e like
-- Feedbacks com Mensagens Contendo "bom" do Mês de Junho
SELECT id_feedback as "Id",
nome_feedback as "Nome",
tel_feedback as "Telefone",
email_feedback as "E-mail",
msg_feedback as "Mensagem", 
nota_feedback as "Avaliação",
data_feedback as "Data"
FROM T_Form_Feedback
WHERE data_feedback BETWEEN TO_TIMESTAMP('2024-06-01 00:00:00', 'YYYY-MM-DD HH24:MI:SS') AND TO_TIMESTAMP('2024-06-30 23:59:59', 'YYYY-MM-DD HH24:MI:SS')
AND LOWER(msg_feedback) LIKE '%bom%'
ORDER BY "Nome";

